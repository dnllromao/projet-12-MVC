		<div class="content">
			<h2>Contraintes</h2>

			<div class="question-group">
				<p class="question">Quelles sont les personnes avec qui tu es amené à entrer en relation ?</p>
				<div class="answer">
					<ul>
						<li>D'autres développeurs (front-end + back-end)</li>
						<li>Des designers</li>
						<li>Des clients (petits, moyens, grands)</li>
						<li>Des prestataires pour ces clients (avec lequels je dois me coordonner)</li>
						<li>Un comptable (à cause de mon status d'indépendant)</li>
					</ul>
				</div>
			</div>

			<div class="question-group">
				<p class="question">Y a-t-il des conditions de travail spécifiques ? Des contraintes physiques ?</p>
				<div class="answer">
					<p>Pas vraiment. Il est possible de travailler depuis un peu n'importe où, à n'importe quelle heure. C'est un peu plus compliqué lorsqu'on travaille en équipe, mais beaucoup y arrivent tout de même.</p>
					<p>Je n'identifie pas non plus de contraintes physiques puisque les technologies (et les outils de développement) sont de plus en plus accessibles aux personnes handicapées physiques, aveugles ou autres. Par exemple, nous collaborons actuellement avec un développeur malvoyant qui nous aide à rendre les produits que nous développons accessibles à un maximum de personnes.</p>
				</div>
				
			</div>
			
			<div class="question-group">
				<p class="question">Quels sont les problèmes auxquels tu peux être confronté ?</p>
				<div class="answer">
					<p>Je n'ai jamais eu de problème conséquent, mais ceux qui m'ont posé le plus de souci sont parfois liés à la définition des responsabilités du développeur lorsqu'un produit est ensuite mis en production. Si un bug venait à être identifié, le développeur est-il responsable des conséquences qui en découleront? C'est particulièrement important lors de projets e-commerce.</p>
				</div>
			</div>

		</div>
	</section>


