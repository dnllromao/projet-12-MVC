		<div class="content">
			<h2>Le marché de l'emploi</h2>

			<div class="question-group">
				<p class="question">Quelles sont les évolutions possibles selon toi ?</p>
				<div class="answer">
					<p>C'est une question très difficile à répondre... Ce dont je suis sûr cependant est que la demande en développeurs ne risque pas de diminuer de sitôt.</p>
				</div>
			</div>

			<div class="question-group">
				<p class="question">Quel est l'état du marché de l'emploi pour ce métier en général selon toi ? Dans ta zone géographique ?</p>
				<div class="answer">
					<p>Il y a une forte demande dans les profils liés au développement front-end, pour des missions parfois courtes, parfois très longues. Beaucoup d'agences qui étaient à l'origine orienté "print" se rendent compte qu'elles ne peuvent plus survivre sans un pôle digital et sont donc à la recherche de personnes qui peuvent les aider en design (UI/UX), dev front-end ou, idéalement, les deux.</p>
					<p>En ce qui concerne le backend c'est un petit peu différent car la plupart des gens ne savent pas exactement de quoi il s'agit. Souvent, ils confient ce rôle au développeur front-end (ce n'est évidemment pas la bonne solution). Seules les sociétés un peu plus "solides" ou les sociétés de consulting proposent des offres d'emploi intéressantes pour les développeurs backend.</p>
					<p>Dans tous les cas, il ne faut pas attendre une offre d'emploi pour postuler. Si une société n'a pas besoin de toi aujourd'hui, ils auront plus que probablement besoin de toi demain. Au pire, ça fait un contact en plus.</p>
				</div>
				
			</div>
			
			<div class="question-group">
				<p class="question">Existe-t-il des difficultés de recrutement dans ton métier ?</p>
				<div class="answer">
					<p>Je n'ai jamais rencontré de souci personnellement, même si je n'ai pas trouvé un boulot directement. J'étais le seul frein ceci dit, car je ne démarchais pas assez.</p>
				</div>
			</div>

			<div class="question-group">
				<p class="question">As-tu déjà été démarché par un recruteur ?</p>
				<div class="answer">
					<p>Oui, très souvent via LinkedIn. Les fondateurs de startups sont aussi plutôt actifs, même en Belgique. Avoir un bon réseau autour de soi est donc très utile.</p>
				</div>
			</div>

		</div>
	</section>

