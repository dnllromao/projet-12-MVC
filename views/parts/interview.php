			<h2>Conseils...</h2>

			<div class="question-group">
				<p class="question">Y a-t-il des personnes à qui tu déconseillerais de faire ce métier ?</p>
				<div class="answer">
					<p>Non. Le boulot évolue tellement au quotidien que je n'oserais pas le déconseiller à qui que ce soit. Je n'aurais pas d'argument. Tout le monde peut y trouver son compte.</p>
					<p>Evidemment, il ne faut pas avoir peur d'être derrière un écran une bonne partie de la journée.</p>
				</div>
			</div>

			<div class="question-group">
				<p class="question">Quels conseils me donnerais-tu ?</p>
				<div class="answer">
					<p>J'imagine que le plus gros challenge qui t'attendra à la fin de tes études sera de trouver du boulot. Mon conseil serait de ne pas hésiter à démarcher les employeurs potentiels toi-même (ou les clients potentiels si tu souhaites te lancer à ton propre compte).</p>
					<p>Forge-toi aussi un réseau solide (en commençant par les autres personnes présent à la formation BeCode) et n'hésite pas à partager sur les réseaux sociaux ce que tu développes et qui a le potentiel d'intéresser d'autres développeurs (via Github). La communauté appréciera et te rendra la pareille à un moment ou à un autre.</p>
				</div>
				
			</div>

		</div>
